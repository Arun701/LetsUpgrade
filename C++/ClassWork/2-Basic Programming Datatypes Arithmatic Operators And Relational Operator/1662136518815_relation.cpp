#include<iostream>
using namespace std;

int main()
{
    int a,b;

    a=34;
    b=25;

    cout<<endl<<"compare a > b "<<(a>b);
    cout<<endl<<"compare a < b "<<(a<b);
    cout<<endl<<"compare a = b "<<(a==b);
    cout<<endl<<"compare a <> b "<<(a!=b);
    cout<<endl<<"compare a >= b "<<(a>=b);
    cout<<endl<<"compare a <= b "<<(a<=b);

}